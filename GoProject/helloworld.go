package main

import "fmt"

/*
อธิบายเกี่ยวกับคำสั่ง Goto ในภาษา Go ซึ่งใช้เพื่อย้ายการควบคุมโปรแกรมไปยังคำสั่งที่มีป้ายกำกับ (Label) ในโปรแกรม โดยป้ายกำกับต้องเป็นตัวระบุที่ถูกต้องและวางไว้ก่อนคำสั่งที่จะย้ายการควบคุมไป ตัวอย่างโปรแกรมแสดงการใช้คำสั่ง Goto เพื่อข้ามการพิมพ์เลข 3 และกระโดดไปทำงานที่ป้ายกำกับ Lable1 ทันที
*/

func main() {
	var a int = 1
	lable1:
	      for a <= 5 {
			if a == 3 {
				a = a + 1
				goto lable1
			}
			fmt.Printf("Number is: %d\n", a)
			a++
		  }
}

/*
1. `package main` และ `import "fmt"` เป็นส่วนของการประกาศแพ็คเกจและการนำเข้าแพ็คเกจ fmt สำหรับใช้งานฟังก์ชันพิมพ์ข้อความ
2. `func main()` เป็นฟังก์ชันหลักที่จะถูกเรียกเมื่อโปรแกรมเริ่มทำงาน
3. `var a int = 1` ประกาศตัวแปร a เป็นจำนวนเต็มและกำหนดค่าเริ่มต้นเป็น 1
4. `Lable1:` เป็นการประกาศป้ายกำกับ (Label) ชื่อ Lable1
5. `for a <= 5` เป็นลูป for ที่ทำงานซ้ำจนกว่า a จะมากกว่าหรือเท่ากับ 5
6. `if a == 3` ตรวจสอบว่า a มีค่าเท่ากับ 3 หรือไม่
7. `a = a + 1` เพิ่มค่า a ขึ้น 1 หากเงื่อนไขในบรรทัดที่ 6 เป็นจริง
8. `goto Lable1` ย้ายการควบคุมโปรแกรมกลับไปที่ป้ายกำกับ Lable1 ทันที ข้ามคำสั่งที่เหลือในลูป
9. `fmt.Printf("Number is: %d\n", a)` พิมพ์ข้อความ "Number is: " ตามด้วยค่า a 
10. `a++` เพิ่มค่า a ขึ้น 1 หลังจากแต่ละรอบของลูป 

เมื่อรันโปรแกรมนี้ ผลลัพธ์ที่ได้จะเป็น:
Number is: 1
Number is: 2
Number is: 4
Number is: 5
*/
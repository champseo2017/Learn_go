package main

import (
	"fmt"
	"time"
)

/*
บทความนี้อธิบายเกี่ยวกับการสร้างลูปไม่มีที่สิ้นสุด (Infinite Loop) ในภาษา Go โดยการละเว้นเงื่อนไขของลูป for หรือใช้ค่าบูลีนเป็น true ตัวอย่างโปรแกรมแสดงการใช้ลูปไม่มีที่สิ้นสุดเพื่อพิมพ์ข้อความ "Infinite Loop" ซ้ำไปเรื่อยๆ โดยมีการหน่วงเวลาด้วยฟังก์ชัน time.Sleep() เป็นเวลา 1 วินาที
*/

func main() {
	for true {
		fmt.Println("Infinite Loop")
		time.Sleep(time.Second)
	}
}

/*
1. `package main` และ `import ("fmt" "time")` เป็นส่วนของการประกาศแพ็คเกจและการนำเข้าแพ็คเกจ fmt และ time สำหรับใช้งานฟังก์ชันพิมพ์ข้อความและฟังก์ชันหน่วงเวลา
2. `func main()` เป็นฟังก์ชันหลักที่จะถูกเรียกเมื่อโปรแกรมเริ่มทำงาน
3. `for true` เป็นลูปไม่มีที่สิ้นสุด เนื่องจากเงื่อนไขเป็น true เสมอ ทำให้ลูปทำงานไปเรื่อยๆ ไม่มีทางหยุด
4. `fmt.Println("Infinite Loop")` พิมพ์ข้อความ "Infinite Loop"
5. `time.Sleep(time.Second)` หน่วงเวลาเป็นเวลา 1 วินาที ก่อนที่จะเริ่มรอบถัดไปของลูป

เมื่อรันโปรแกรมนี้ ผลลัพธ์ที่ได้จะเป็น:
Infinite Loop
Infinite Loop
Infinite Loop
Infinite Loop
Infinite Loop
...
...

โปรแกรมจะพิมพ์ข้อความ "Infinite Loop" ซ้ำไปเรื่อยๆ ทุกๆ 1 วินาที จนกว่าจะมีการหยุดโปรแกรมด้วยตนเอง เนื่องจากเป็นลูปไม่มีที่สิ้นสุด
*/
package main

import "fmt"

/*
อธิบายเกี่ยวกับคำสั่ง break ในภาษา Go ซึ่งใช้เพื่อยุติการทำงานของลูปหรือคำสั่งที่มันปรากฏอยู่ คำสั่ง break จะหยุดการทำงานของลูปปัจจุบันทันที ตัวอย่างโปรแกรมแสดงการใช้คำสั่ง break ในลูป for เพื่อพิมพ์ตัวเลขตั้งแต่ 1 ถึง 5 แต่เมื่อค่า x เท่ากับ 4 จะเจอคำสั่ง break และออกจากลูปทันที
*/

func main() {
	for x:= 1; x <= 5; x++ {
		fmt.Println("Number:", x)
		if x == 4 {
			break
		}
	}
}

/*
1. `package main` บอกว่าไฟล์นี้อยู่ในแพ็คเกจหลักของโปรแกรม
2. `import "fmt"` เป็นการนำเข้าแพ็คเกจ fmt เพื่อใช้งานฟังก์ชันในการพิมพ์ข้อความ
3. `func main()` เป็นฟังก์ชันหลักที่จะถูกเรียกเมื่อโปรแกรมเริ่มทำงาน
4. `for x := 1; x <= 5; x++` เป็นลูป for ที่กำหนดให้ x เริ่มต้นที่ 1 และวนลูปจนกว่า x จะมากกว่าหรือเท่ากับ 5 โดยเพิ่มค่า x ทีละ 1 ในแต่ละรอบ
5. `fmt.Println("Number:", x)` พิมพ์ข้อความ "Number:" ตามด้วยค่า x ในแต่ละรอบของลูป
6. `if x == 4` ตรวจสอบว่าค่า x เท่ากับ 4 หรือไม่
7. `break` หากเงื่อนไขในบรรทัดที่ 6 เป็นจริง จะออกจากลูปทันทีโดยไม่ทำคำสั่งที่เหลือในลูป

เมื่อรันโปรแกรมนี้ ผลลัพธ์ที่ได้จะเป็น:
Number: 1
Number: 2
Number: 3
Number: 4
*/
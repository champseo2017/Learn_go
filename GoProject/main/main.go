package main

import "fmt"

/*
ในภาษา Go เราสามารถกำหนดฟังก์ชันให้กับตัวแปรได้ และเรียกใช้งานฟังก์ชันผ่านตัวแปรนั้นได้เหมือนกับการเรียกใช้ฟังก์ชันปกติ ลองดูตัวอย่างโค้ดดังนี้ครับ
*/

func Declare_Func_As_Variable() {
	a := func() {
		fmt.Println("hello world")
	}
	a() // เรียกใช้ฟังก์ชันที่กำหนดให้กับตัวแปร a
}

func main() {
	Declare_Func_As_Variable()
}

/*
1. เราประกาศฟังก์ชัน `Declare_Func_As_Variable` ขึ้นมา
2. ภายในฟังก์ชัน เรากำหนดฟังก์ชันแบบ anonymous (ไม่ระบุชื่อ) ให้กับตัวแปร `a`
   - ฟังก์ชันนี้จะพิมพ์ข้อความ "hello world" ออกมาทาง console
3. เรียกใช้ฟังก์ชันที่กำหนดให้กับตัวแปร `a` โดยการเขียน `a()`
4. ในฟังก์ชัน `main` เรียกใช้ฟังก์ชัน `Declare_Func_As_Variable` เพื่อให้โปรแกรมทำงาน
*/

package main

import (
	"fmt"
)

/*
เกี่ยวกับตัวดำเนินการทางคณิตศาสตร์กับพอยน์เตอร์ในภาษา Go:

1. ภาษา Go ไม่อนุญาตให้ใช้ตัวดำเนินการทางคณิตศาสตร์กับพอยน์เตอร์ เพื่อความง่ายและความปลอดภัย
2. การใช้ตัวดำเนินการทางคณิตศาสตร์กับพอยน์เตอร์อาจทำให้เกิดที่อยู่ที่ไม่ถูกต้อง ซึ่งเป็นอันตราย
3. ในภาษาตระกูล C การใช้ตัวดำเนินการทางคณิตศาสตร์กับพอยน์เตอร์มีประโยชน์ในการทำซ้ำอาร์เรย์อย่างรวดเร็ว
4. ในปัจจุบัน การทำซ้ำอาร์เรย์โดยใช้ดัชนีมีประสิทธิภาพมากกว่าการใช้ตัวดำเนินการทางคณิตศาสตร์กับพอยน์เตอร์
5. ในภาษา Go สามารถทำซ้ำอาร์เรย์โดยใช้ดัชนีได้อย่างมีประสิทธิภาพ โดยไม่จำเป็นต้องใช้ตัวดำเนินการทางคณิตศาสตร์กับพอยน์เตอร์

การไม่อนุญาตให้ใช้ตัวดำเนินการทางคณิตศาสตร์กับพอยน์เตอร์ในภาษา Go ช่วยลดความซับซ้อนและเพิ่มความปลอดภัยในการเขียนโปรแกรม ในขณะที่ยังคงสามารถทำซ้ำอาร์เรย์ได้อย่างมีประสิทธิภาพโดยใช้ดัชนี
*/

func main() {
    numbers := [5]int{1, 2, 3, 4, 5}
    
    // ทำซ้ำอาร์เรย์โดยใช้ดัชนี
    for i := 0; i < len(numbers); i++ {
        fmt.Println(numbers[i])
    }
}

package main

import (
	"fmt"
)

/*
เมื่อส่งอาร์เรย์เข้าไปในฟังก์ชันโดยวิธี Pass by Value จะเป็นการสร้างสำเนาของอาร์เรย์ขึ้นมาใหม่ แล้วส่งสำเนานั้นเข้าไปในฟังก์ชัน ดังนั้นการเปลี่ยนแปลงค่าในอาร์เรย์ภายในฟังก์ชันจะไม่ส่งผลกระทบต่ออาร์เรย์ต้นทาง
*/

// ฟังก์ชัน Pass_Array_Example รับพารามิเตอร์เป็นอาร์เรย์ของ int ขนาด 3
func Pass_Array_Example(arr [3]int) {
	// เปลี่ยนค่าในอาร์เรย์ตำแหน่งแรกเป็น 2000
	arr[0] = 2000
}

func main() {
	// กำหนดค่าเริ่มต้นให้อาร์เรย์ res
	res := [...]int{10, 20, 30}

	// ส่งอาร์เรย์ res เข้าไปในฟังก์ชัน Pass_Array_Example
	Pass_Array_Example(res)

	// แสดงค่าในอาร์เรย์ res ซึ่งยังคงเป็นค่าเดิม
	fmt.Println(res)
}

/*
จะเห็นได้ว่า แม้ภายในฟังก์ชัน Pass_Array_Example จะมีการเปลี่ยนแปลงค่าในอาร์เรย์ตำแหน่งแรกเป็น 2000 แต่ค่าในอาร์เรย์ res ก็ยังคงเป็น [10 20 30] เนื่องจากเป็นการส่งสำเนาของอาร์เรย์เข้าไปในฟังก์ชัน การเปลี่ยนแปลงจึงไม่ส่งผลกระทบต่ออาร์เรย์ต้นทาง
*/
